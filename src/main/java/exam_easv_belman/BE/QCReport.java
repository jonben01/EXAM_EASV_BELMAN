package exam_easv_belman.BE;

public class QCReport {
    private String qcName;
    private String qcSignaturePath;
    private String operatorName;
    private String orderNumber;
    private String productNumber;
    private String imagePath;
    private String imageComment;

    public String getQcName() {
        return qcName;
    }

    public void setQcName(String qcName) {
        this.qcName = qcName;
    }

    public String getQcSignaturePath() {
        return qcSignaturePath;
    }

    public void setQcSignaturePath(String qcSignaturePath) {
        this.qcSignaturePath = qcSignaturePath;
    }

    public String getOperatorName() {
        return operatorName;
    }

    public void setOperatorName(String operatorName) {
        this.operatorName = operatorName;
    }

    public String getOrderNumber() {
        return orderNumber;
    }

    public void setOrderNumber(String orderNumber) {
        this.orderNumber = orderNumber;
    }

    public String getProductNumber() {
        return productNumber;
    }

    public void setProductNumber(String productNumber) {
        this.productNumber = productNumber;
    }

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String imagePath) {
        this.imagePath = imagePath;
    }

    public String getImageComment() {
        return imageComment;
    }

    public void setImageComment(String imageComment) {
        this.imageComment = imageComment;
    }

}
